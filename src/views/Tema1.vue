        .h5 Autonomía progresiva 
        p Este principio hace alusión al proceso en el que las niñas, niños y adolescentes van adquiriendo derechos y deberes según su edad y etapa de vida, mientras se desarrollan sus capacidades y autonomía para tomar decisiones y adquirir conciencia de sus consecuencias. En otras palabras, a mayor grado de autonomía que pueda tener la niña, niño o adolescente, mayor será el nivel de derechos que pueda ejercer como individuo (Gómez de la Torre, 2018). En ese sentido, se considera como progresiva en la medida que un niño de 3 años no puede tener el mismo nivel de autonomía y ejercicio de derechos que un adolescente de 16; esto debido a que el adolescente cuenta con un mayor nivel de comprensión de sus acciones y sus consecuencias; y, así mismo, tiene más autonomía que un niño o niña en la primera infancia.
