    p.mb-4 Durante este proceso los padres, madres o tutores cumplen un rol fundamental al guiar y acompañar a la niña, niño o adolescente a través del cuidado, exigiendo sus derechos y velando por su cumplimiento, en especial cuando se encuentran en un nivel de desarrollo temprano. Gradualmente los padres o responsables van reduciendo su representatividad mientras el niño va creciendo. 

    p.mb-5 En consecuencia, se evidencia que los niños, niñas y adolescentes van adquiriendo autonomía de forma gradual y siempre con el acompañamiento y orientación de los padres que ajustan su rol según el proceso de desarrollo del individuo. Igualmente, es necesario tener en cuenta los contextos particulares en donde se desenvuelve la niñez y la adolescencia para determinar, de mejor manera, el nivel de autonomía y ejercicio de derechos adecuado.    

