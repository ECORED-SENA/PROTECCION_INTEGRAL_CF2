    p En épocas anteriores esta posibilidad era negada al considerarse que la infancia y la adolescencia no podían tener una representación jurídica plena por estar bajo el cuidado de padres, madres o tutores y, en consecuencia, no poseían el derecho a elegir y participar libremente. Con esta visión, los niños, niñas y adolescentes eran considerados un objeto de cuidado que solo al convertirse en adultos podrían ejercer plenamente su rol como ciudadanos (Gómez de la Torre, 2018). A este enfoque se le conoce como “paradigma de la protección tutelar” (De la Iglesia et ál., 2008) y actualmente no hace parte de los marcos normativos del Estado colombiano. 
