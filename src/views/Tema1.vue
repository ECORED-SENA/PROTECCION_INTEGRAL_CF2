      .col-12.col-lg
        .h5 Interés superior del niño
        p.mb-4 Este principio hace alusión a la obligatoriedad que tienen todas las instancias sociales y judiciales en satisfacer y hacer cumplir los derechos de niñas, niños y adolescentes al momento de presentarse cualquier situación donde puedan ser vulnerados o puestos en riesgo. El interés superior del niño surgió como elemento rector desde la Convención sobre los Derechos del Niño y se ha mantenido hasta la actualidad. 

        p El interés superior lleva a priorizar los derechos de la infancia y la adolescencia sobre otras consideraciones cuando un juez o una entidad estatal decidan actuar en la resolución de un conflicto (ICBF, 2013). Por ejemplo, si un niño o niña requiere atención médica puede obtenerla a pesar de no contar con un registro en alguna Empresa Prestadora de Salud (EPS) o con recursos económicos, esto debido a que el interés superior del niño o la niña y la protección de su derecho a la vida están por encima de los requerimientos puntuales del sistema de salud en casos de urgencia o extrema necesidad. 

    p.mb-5 De igual manera, el interés superior del niño impacta otras áreas de la vida como las políticas públicas, la educación, la vivienda y alimentación, entre otros. Por ende, este principio se ha convertido en un elemento jurídico fundamental transversal a todas las interpretaciones y disposiciones de las leyes y el funcionamiento de las instituciones del Estado.

