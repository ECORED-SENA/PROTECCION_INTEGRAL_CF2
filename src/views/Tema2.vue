      #t_2_2.h4 2.2 La familia, la sociedad y el Estado como garantes de derecho
    
    p.mb-4 Como se mencionó anteriormente, la implicación de nuevos actores en la protección integral ha producido una transformación en las nociones clásicas de responsabilidad. Sobre este tema, muchas personas tienden a pensar que la protección de los derechos de niños, niñas y adolescentes recae exclusivamente en sus padres o tutores y en el Estado. Sin embargo, la realidad es que toda la sociedad en su conjunto tiene la misma obligación de garantizar el ejercicio de los derechos de los niños, niñas y adolescentes y vigilar su implementación a lo largo de tiempo, esto incluye a instituciones educativas, empresas privadas y a la ciudadanía en general (ICBF, 2018). 

    p A esta asignación de roles se le conoce como #[strong principio de corresponsabilidad], el cual tiene una gran relevancia en la planeación de políticas para la protección de derechos de niños, niñas y adolescentes. 

